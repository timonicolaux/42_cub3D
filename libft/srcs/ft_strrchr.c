#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	size_s;
	int		i;

	size_s = ft_strlen(s);
	i = size_s;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (0);
}
